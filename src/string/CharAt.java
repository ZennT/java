package string;

/**
 * returns the characterthat is located at specific index
 * Syntax: stringName.charAt(int index);
 */

public class CharAt {
    public static void main(String[] args) {
        String corona = "Coronavirus vaccine should come out before 2021.";
        System.out.println("The caharacter located at index 6 is: " + corona.charAt(6));

    }
}
